#include "IRefCount.h"

//////////////////////////////////////////////////////////////////////////////////////////////////////
// RefCount
//////////////////////////////////////////////////////////////////////////////////////////////////////
IRefCount::IRefCount() : m_RefNum(0)
{
}

void IRefCount::Add()
{
	++m_RefNum;
}

void IRefCount::Dec()
{
	--m_RefNum;
	if (0 >= m_RefNum)
	{
		delete this;
	}
}